Map::Map(RenderWindow *window)
    : m_Window(nullptr), m_Dirt(nullptr), m_Grass(nullptr), m_Water(nullptr)
{
    m_Window = window;

    if (m_Window == nullptr)
    {
        std::cout << "[Error] : Window is not loaded" << std::endl;
    }

    m_Dirt = m_Window->loadTexture("res/gfx/Dirt.png");
    m_Grass = m_Window->loadTexture("res/gfx/Grass.png");
    m_Water = m_Window->loadTexture("res/gfx/Water.png");
    if (m_Dirt == nullptr || m_Grass == nullptr || m_Water == nullptr)
    {
        std::cout << "[Error] : Map's Texture is not loaded" << std::endl;
    }

    LoadMap();
    DrawMap();
}

Map::~Map() {}

void Map::LoadMap()
{
    for (int row = 0; row < 20; row++)
    {
        for (int col = 0; col < 25; col++)
        {
            m_Map[row][col] = lv1[row][col];
        }
    }
}
void Map::DrawMap() const
{
    for (int row = 0; row < 20; row++)
    {
        for (int col = 0; col < 25; col++)
        {
            switch (m_Map[row][col])
            {
            case TypeDirt:
            {
                Entity piece(Vector2f(col * 32, row * 32), m_Dirt);
                m_Window->render(piece);
                break;
            }
            case TypeGrass:
            {
                Entity piece(Vector2f(col * 32, row * 32), m_Grass);
                m_Window->render(piece);
                break;
            }
            case TypeWater:
            {
                Entity piece(Vector2f(col * 32, row * 32), m_Water);
                m_Window->render(piece);
                break;
            }
            default:
            {
                break;
            }
            }
        }
    }
}
